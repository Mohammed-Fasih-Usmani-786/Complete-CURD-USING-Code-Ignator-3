							<h2>Pre Construction TermiCon Services – As IS 6313 PartII </h2>
								<h4>A - TREATMENT OF SOIL</h4>
								<p>The chemical emulsion of Termiticides  as per BIS:6313 (Part 2) : 2013 shall be applied uniformly at the prescribed rate in all the stages to creating the continuous chemical barrier to the soil before laying soling and plain cement concrete (PCC). The treatment shall be carried out in the following stages:-</p>
								<ul>
								<li>Treatment of top Surface of Plinth Filling</li>
<li> Treatment at Junction of Wall and Floor</li>
<li>Treatment of Soil along External Perimeter of Building</li>
<li> Sub soil area (except plinth area</li>
<li> Treatment of sidewalls by spraying pesticide</li>
</ul>

<h4>B- TREATMENT OF WOODWORK OF ALL  FLOORS</h4>
								
								<ul>
								<li>Treatment of door & window frames</li>
<li>Treatment of wood work, board & ply board</li>
<li>Treatment of walls</li></ul>

<h4>C- OPTIONAL – TERMITUBE SYSTEM</h4>
								<p>Termitube installation will be completed at flooring level, will be tested in the presence of your representative and the duly completed structure will be handed over to you by fixing the Junction Box, which could be opened, used for periodic recharging of Termiticides in future.</p>
								<p><b>Installation of Termitube System:</b></p>
								<ul>
								<li>After levelling of soil within plinth area of building, 100 x 100 mm channel in a grid way will be made with the help of gaiti along with plinth walls. </li>
<li>Laying the Termitube by using jointers in the channels all along the plinth walls (both sides).</li>
<li>Levelling by filling the sand in the channels.</li>
<li>After this step, sand-bed or PCC can be done by your civil contractors.</li>
<li>Testing of Termitube System by injecting the chemical emulsion with the help of motorised spray pump wiill be carried out 2 times (before and after PCC or send-bed work.</li>
